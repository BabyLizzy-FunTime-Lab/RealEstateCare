<script>
import {IonList, IonItem, IonInput,
  IonListHeader, IonLabel, IonButton,
  IonAlert, IonContent} from "@ionic/vue";
import {useLoginStore} from "@/stores/LoginStore.js";

export default {
  name: "LoginForm",
  components: {IonList, IonItem, IonInput,
    IonListHeader, IonLabel, IonButton, IonAlert, IonContent},
  data() {
    return {
      loginStore: useLoginStore(),
      username: "",
      password: "",
    }
  },
}
</script>

<template>
  <ion-alert
      :isOpen="loginStore.errorMessage"
      header="Problem!"
      sub-header="There was a login error."
      :message="loginStore.errorMessage"
      :buttons="['OK']"
      @didDismiss="loginStore.setErrorMessage(null)"
  />
  <ion-list>
    <ion-list-header color="primary">
      <ion-label>
        <h2>Login</h2>
      </ion-label>
    </ion-list-header>
    <ion-item>
      <ion-input type="text"
                 v-model="username"
                 label="username:"
                 @keyup.enter="loginStore.fetchUser(username, password)"/>
    </ion-item>
    <ion-item>
      <ion-input type="password"
                 v-model="password"
                 label="password:"
                 @keyup.enter="loginStore.fetchUser(username, password)"/>
    </ion-item>
    <ion-button expand="full"
                color="primary"
                @click="loginStore.fetchUser(username, password)">Go!</ion-button>
  </ion-list>
</template>

<style scoped lang="scss">
ion-list-header {
  margin-bottom: .5em;
}
ion-button {
  margin-top: 2em;
}
